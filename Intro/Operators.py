# Operator is a symbol which is used to perform certain operations between variables

a = 5
b = 2

# Arithmetic operator
print('--Arithmetic Operator--')
print(a + b)
print(a - b)
print(a / b)
print(a // b)  # quotient
print(a % b)  # remainder
print(a * b)
print(a ** b)  # 5 to the power of 2

# Relational Operators
print('--Relational Operator--')
print(a == b)
print(a <= b)
print(a >= b)
print(a != b)
print(a < b)
print(a > b)

# Logical Operators
print('--Logical Operator--')
a = True
b = False
print(a and b)
print(a or b)
print(not a)

